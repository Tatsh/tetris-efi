UINT8 board[BOARD_HEIGHT][BOARD_WIDTH];

int collision(int piece, int rot, int px, int py) {
    for (int r = 0; r < 4; r++) {
        for (int c = 0; c < 4; c++) {
            if (!SHAPES[piece][rot][r][c]) {
                continue;
            }
            int bx = px + c;
            int by = py + r;
            if (bx < 0 || bx >= BOARD_WIDTH || by >= BOARD_HEIGHT) {
                return 1;
            }
            if (by >= 0 && board[by][bx]) {
                return 1;
            }
        }
    }
    return 0;
}
